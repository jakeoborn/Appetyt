// Script logic: read index.html, find AUSTIN_DATA, dedup, insert
// ────────────────────────────────────────────────────────────────────
const html = fs.readFileSync(HTML_PATH, 'utf8');

const AUSTIN_MARKER = 'const AUSTIN_DATA=';
const austinStart = html.indexOf(AUSTIN_MARKER);
if (austinStart === -1) throw new Error('Cannot find AUSTIN_DATA in index.html');
const nextConst = html.indexOf('const ', austinStart + AUSTIN_MARKER.length);
const austinChunk = html.slice(austinStart, nextConst);

// Get existing names (lowercase for dedup)
const existingNames = new Set(
  [...austinChunk.matchAll(/"name":"([^"]+)"/g)].map(m => m[1].toLowerCase())
);

// Get max ID
const maxId = Math.max(...[...austinChunk.matchAll(/"id":(\d+)/g)].map(m => parseInt(m[1])));
let nextId = maxId + 1;

// Filter duplicates
const toAdd = NEW_RESTAURANTS.filter(r => {
  const lower = r.name.toLowerCase();
  if (existingNames.has(lower)) {
    console.log(`SKIP (duplicate): ${r.name}`);
    return false;
  }
  // Also check substring match for close variants
  const found = [...existingNames].find(n => n.includes(lower) || lower.includes(n));
  if (found) {
    console.log(`SKIP (similar to "${found}"): ${r.name}`);
    return false;
  }
  return true;
});

console.log(`\nAdding ${toAdd.length} new restaurants (starting at ID ${nextId})`);

// Build JSON entries
const entries = toAdd.map(r => {
  const entry = {
    phone: r.phone || '',
    bestOf: [],
    busyness: null,
    waitTime: null,
    popularTimes: null,
    lastUpdated: null,
    trending: false,
    group: r.group || '',
    suburb: ['Cedar Park','Dripping Springs','Westlake','Lake Travis','Driftwood'].includes(r.neighborhood),
    reserveUrl: '',
    menuUrl: '',
    id: nextId++,
    name: r.name,
    cuisine: r.cuisine,
    neighborhood: r.neighborhood,
    score: r.score,
    price: r.price,
    tags: r.tags,
    indicators: [],
    hh: r.hh || '',
    reservation: r.reservation || '',
    awards: r.awards || '',
    description: r.description,
    dishes: r.dishes,
    address: r.address,
    hours: r.hours || '',
    lat: r.lat,
    lng: r.lng,
    res_tier: r.reservation === 'Resy' || r.reservation === 'Tock' ? 2 : r.reservation === 'OpenTable' ? 1 : 0,
    instagram: r.instagram || '',
    website: r.website || '',
    photoUrl: '',
    reels: []
  };
  return JSON.stringify(entry);
});

// Find insertion point: just before the closing `];` of AUSTIN_DATA
// The pattern is the last `}` in the array, then optionally whitespace, then `]`
const arrayStart = html.indexOf('[', austinStart);
if (arrayStart === -1) throw new Error('Cannot find array start for AUSTIN_DATA');

// Find the matching `]` by counting brackets
let depth = 0;
let arrayEnd = -1;
for (let i = arrayStart; i < austinStart + austinChunk.length; i++) {
  if (html[i] === '[') depth++;
  else if (html[i] === ']') {
    depth--;
    if (depth === 0) { arrayEnd = i; break; }
  }
}
if (arrayEnd === -1) throw new Error('Cannot find array end for AUSTIN_DATA');

// Insert before the closing `]`
const insertionPoint = arrayEnd;
const newHtml = html.slice(0, insertionPoint) + ',' + entries.join(',') + html.slice(insertionPoint);

fs.writeFileSync(HTML_PATH, newHtml);
console.log(`\nDone. ${toAdd.length} restaurants added to AUSTIN_DATA (IDs ${maxId + 1}–${nextId - 1}).`);
